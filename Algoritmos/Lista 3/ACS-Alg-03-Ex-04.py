lados = int(input("Digite a quantidade de lados do polígono: "))

if lados == 3:
    print("Triângulo")
elif lados == 4:
    print("Quadrado")
elif lados == 5:
    print("Pentágono")
elif lados == 6:
    print("Hexágono")
elif lados == 7:
    print("Heptágono")
elif lados == 8:
    print("Octógono")
elif lados == 9:
    print("Eneágono")
elif lados == 10:
    print("Decágono")
else:
    print("ERRO")
    